package testRestAPI;


/*
 * POJO= plain old java object
 */
public class PojoData 
{
	//Encapsulation= data+function
	
	String name;
	String job;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	

}
